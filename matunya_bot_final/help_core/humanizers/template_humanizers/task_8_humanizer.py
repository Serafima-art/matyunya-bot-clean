def humanize(solution_core: Dict[str, Any]) -> str:
    """
    –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è. –ü—Ä–µ–≤—Ä–∞—â–∞–µ—Ç solution_core –≤ HTML.
    """
    parts: List[str] = []

    # 1. –ò–¥–µ—è —Ä–µ—à–µ–Ω–∏—è (–ò–°–ü–†–ê–í–õ–ï–ù–û: –¥–æ–±–∞–≤–ª–µ–Ω–∞ –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤)
    idea_key = solution_core.get("explanation_idea_key")
    idea_params = solution_core.get("explanation_idea_params") or {}

    idea_text = ""
    if idea_key:
        raw_template = IDEA_TEMPLATES.get(idea_key, IDEA_TEMPLATES["IDEA_GENERIC"])
        try:
            # –í–æ—Ç –∑–¥–µ—Å—å –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –º–∞–≥–∏—è: {location} –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç—Å—è –≤ "—á–∏—Å–ª–∏—Ç–µ–ª–µ"
            idea_text = raw_template.format(**idea_params)
        except Exception:
            # –ï—Å–ª–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ –Ω–µ —Ö–≤–∞—Ç–∏–ª–æ, –≤—ã–≤–æ–¥–∏–º –∫–∞–∫ –µ—Å—Ç—å, —á—Ç–æ–±—ã –Ω–µ —É–ø–∞—Å—Ç—å
            idea_text = raw_template
    else:
        idea_text = str(solution_core.get("explanation_idea") or "")

    if idea_text:
        parts.append(f"üí° <b>–ò–¥–µ—è —Ä–µ—à–µ–Ω–∏—è: </b>{idea_text}")
        parts.append("")

    # 2. –®–∞–≥–∏ (–ë–ï–ó –ò–ó–ú–ï–ù–ï–ù–ò–ô)
    steps = solution_core.get("calculation_steps") or []
    if steps:
        parts.append("ü™ú <b>–ü–æ—à–∞–≥–æ–≤–æ–µ —Ä–µ—à–µ–Ω–∏–µ</b>")
        parts.append("")
        for step in steps:
            parts.append(_render_step(step))
            parts.append("")

    # 3. –û—Ç–≤–µ—Ç (–ë–ï–ó –ò–ó–ú–ï–ù–ï–ù–ò–ô)
    final_answer_val = str((solution_core.get("final_answer") or {}).get("value_display", ""))
    if final_answer_val:
        parts.append(f"üéØ–û—Ç–≤–µ—Ç: <b>{final_answer_val}</b>")
        parts.append("")

    # 4. –û–±—Ä–∞—Ç–∏ –≤–Ω–∏–º–∞–Ω–∏–µ (–ë–ï–ó –ò–ó–ú–ï–ù–ï–ù–ò–ô)
    attention_key = solution_core.get("attention_tips_key")
    attention_params = solution_core.get("attention_tips_params") or {}
    attention_items = []

    if attention_key:
        raw_items = ATTENTION_TEMPLATES.get(attention_key, [])
        for item in raw_items:
            try:
                attention_items.append(item.format(**attention_params))
            except KeyError:
                attention_items.append(item)
    else:
        attention_items = _extract_list(solution_core.get("attention_tips"))

    if attention_items:
        parts.append(_render_attention_block(attention_items))
        parts.append("")

    # 5. –ü–æ–ª–µ–∑–Ω–æ –∑–Ω–∞—Ç—å (Knowledge)
    knowledge_key = solution_core.get("knowledge_tips_key")
    # –ë–µ—Ä–µ–º —Ç–µ –∂–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã, —á—Ç–æ –∏ –¥–ª—è –ò–¥–µ–∏
    know_params = solution_core.get("explanation_idea_params") or {}

    knowledge_items = []

    if knowledge_key:
        raw_items = KNOWLEDGE_TEMPLATES.get(knowledge_key, [])
        for item in raw_items:
            try:
                # –í–û–¢ –¢–£–¢ –ú–ê–ì–ò–Ø –ü–û–î–°–¢–ê–ù–û–í–ö–ò –ß–ò–°–ï–õ
                knowledge_items.append(item.format(**know_params))
            except KeyError:
                knowledge_items.append(item)
    else:
        knowledge_items = _extract_list(solution_core.get("knowledge_tips"))

    if knowledge_items:
        parts.append(_render_knowledge_block(knowledge_items))

    return "\n".join(parts).strip()


