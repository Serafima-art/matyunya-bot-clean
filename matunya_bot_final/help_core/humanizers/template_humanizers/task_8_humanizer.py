def _render_step(step: Dict[str, Any]) -> str:
    number = step.get("step_number", "")

    desc_key = step.get("description_key")
    params = step.get("description_params") or {}

    description = ""
    if desc_key and desc_key in STEP_TEMPLATES:
        try:
            description = STEP_TEMPLATES[desc_key].format(**params)
        except KeyError as e:
            description = f"{desc_key} (–æ—à–∏–±–∫–∞ —à–∞–±–ª–æ–Ω–∞: {e})"
    else:
        description = str(step.get("description") or "")

    formula = step.get("formula_calculation")

    lines = [f"<b>–®–∞–≥ {number}.</b> {description}"]

    if formula:
        # –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ —Å—Ç—Ä–æ–∫–∏, —á—Ç–æ–±—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –∫–∞–∂–¥—É—é –æ—Ç–¥–µ–ª—å–Ω–æ
        f_lines = formula.split("\n")
        for fl in f_lines:
            clean_fl = fl.strip()
            if not clean_fl: continue

            # –õ–û–ì–ò–ö–ê –°–¢–†–ï–õ–û–ö

            # 1. –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –º–∞—Ä–∫–µ—Ä–∞ "text:", —É–±–∏—Ä–∞–µ–º –º–∞—Ä–∫–µ—Ä –∏ –ù–ï —Å—Ç–∞–≤–∏–º —Å—Ç—Ä–µ–ª–∫—É
            if clean_fl.startswith("text:"):
                lines.append(clean_fl[5:].strip())

            # 2. –ï—Å–ª–∏ —Å—Ç—Ä–µ–ª–∫–∞ —É–∂–µ –µ—Å—Ç—å ‚Äî –æ—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å
            elif clean_fl.startswith("‚û°Ô∏è"):
                lines.append(clean_fl)

            # 3. –ï—Å–ª–∏ –µ—Å—Ç—å –∂–∏—Ä–Ω—ã–π —à—Ä–∏—Ñ—Ç (—Ñ–æ—Ä–º—É–ª–∞) ‚Äî –¥–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–µ–ª–∫—É
            elif "<b>" in clean_fl:
                lines.append(f"‚û°Ô∏è {clean_fl}")

            # 4. –ï—Å–ª–∏ –ø—Ä–æ—Å—Ç–æ —Ç–µ–∫—Å—Ç –±–µ–∑ —Ç–µ–≥–æ–≤ ‚Äî –¥–µ–ª–∞–µ–º –∂–∏—Ä–Ω—ã–º —Å–æ —Å—Ç—Ä–µ–ª–∫–æ–π (—Å—Ç–∞–Ω–¥–∞—Ä—Ç)
            else:
                lines.append(f"‚û°Ô∏è <b>{clean_fl}</b>")

    return "\n".join(lines)


def _render_attention_block(tips: Iterable[str]) -> str:
    # –í –º–∞–∫–µ—Ç–µ –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å —ç–º–æ–¥–∑–∏, –Ω–æ –±–µ–∑ –∂–∏—Ä–Ω–æ–≥–æ —à—Ä–∏—Ñ—Ç–∞
    lines = ["‚ú® –û–±—Ä–∞—Ç–∏ –≤–Ω–∏–º–∞–Ω–∏–µ:"]
    for tip in tips:
        # –í –º–∞–∫–µ—Ç–µ –±–µ–∑ –±—É–ª–ª–∏—Ç–æ–≤, –ø—Ä–æ—Å—Ç–æ —Ç–µ–∫—Å—Ç —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏
        lines.append(str(tip))
    return "\n".join(lines)


def _render_knowledge_block(tips: Iterable[str]) -> str:
    lines = ["‚ú® –ü–æ–ª–µ–∑–Ω–æ –∑–Ω–∞—Ç—å", "<tg-spoiler>"]
    for tip in tips:
        lines.append(str(tip))
    lines.append("</tg-spoiler>")
    return "\n".join(lines)


def _extract_list(value: Any) -> List[str]:
    if isinstance(value, list) and value:
        return [str(item) for item in value if item]
    return []
