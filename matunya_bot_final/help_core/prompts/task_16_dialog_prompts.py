# -*- coding: utf-8 -*-
"""
System prompts for Task 16 (Circle Geometry) GPT dialog.

Ключевой принцип:
GPT работает строго в рамках solution_core и шаблонов humanizer'а.
"""

from __future__ import annotations

from textwrap import dedent
from typing import Any, Dict, List, Optional, Union, Sequence

from matunya_bot_final.gpt.prompts.rules_format import RULES_FORMAT
from matunya_bot_final.gpt.prompts.prompt_utils import gender_words, format_history
from matunya_bot_final.gpt.prompts.behavior_protocols import (
    BASE_CHATTER_PERSONA,
    TASK_FOCUS_PROTOCOL,
    DIALOG_HISTORY_PROTOCOL,
)

from matunya_bot_final.gpt.prompts.dialog_prompt import MATYUNYA_DIALOG_STYLE


def get_task_16_dialog_prompt(
    task_data: Dict[str, Any],
    solution_core: Dict[str, Any],
    dialog_history: List[Dict[str, Any]],
    student_name: Optional[str] = None,
    gender: Optional[str] = None,
    golden_set: Union[Dict[str, str], Sequence[str], None] = None,
    help_text: Optional[str] = None,
    image_description: Optional[str] = None,
) -> str:
    """
    Формирует системный промпт для GPT-диалога по Заданию 16 (Окружность).

    Ключевая идея:
    — решение УЖЕ выбрано и рассчитано;
    — GPT сопровождает рассуждение ученика;
    — GPT НЕ анализирует текст задачи и НЕ выбирает метод.
    """

    name = student_name or "друг"
    _gw = gender_words(gender)  # оставляем на будущее (не ломаем общую схему)

    # Кому помогаем
    if gender in ["female", "жен", "ж"]:
        pronoun = "ученице"
    else:
        pronoun = "ученику"

    # ------------------------------------------------------------------
    # 1) Контекст задачи (минимальный)
    # ------------------------------------------------------------------
    # В проекте встречаются оба ключа: "text" и "question_text"
    task_text = (
        task_data.get("text")
        or task_data.get("question_text")
        or "задача по окружности"
    )
    pattern = task_data.get("pattern", "unknown")

    # ------------------------------------------------------------------
    # 2) Эталонное решение (ТОЛЬКО ДЛЯ GPT)
    # ------------------------------------------------------------------
    narrative = solution_core.get("explanation_idea", "").replace("IDEA_", "").lower()
    context = solution_core.get("variables", {}) or {}

    # ------------------------------------------------------------------
    # 3) Шпаргалка фактов (НЕ для ученика!)
    # ------------------------------------------------------------------
    steps_hint: List[str] = []
    for key, value in context.items():
        if isinstance(value, (int, float, str)):
            steps_hint.append(f"- {key}: {value}")
    steps_hint_text = "\n".join(steps_hint) if steps_hint else "—"

    # ------------------------------------------------------------------
    # 4) Golden set (если есть)
    # ------------------------------------------------------------------
    golden_block = ""
    if golden_set:
        entries: List[str] = []
        if isinstance(golden_set, dict):
            # иногда golden_set может приходить как {key: phrase}
            for _, phrase in golden_set.items():
                entries.append(f"- {phrase}")
        elif isinstance(golden_set, (list, tuple)):
            for phrase in golden_set:
                entries.append(f"- {phrase}")

        if entries:
            golden_block = (
                "\n--------------------------------------------------------------------\n"
                "# БАЗА ЗНАНИЙ (GOLDEN SET)\n"
                + "\n".join(entries)
            )

    # ------------------------------------------------------------------
    # 5) Уже показанная помощь (КРИТИЧНО)
    # ------------------------------------------------------------------
    help_block = ""
    if help_text:
        help_block = f"""
--------------------------------------------------------------------
# УЖЕ ПОКАЗАННАЯ ПОМОЩЬ УЧЕНИКУ

Ученик <b>{name}</b> УЖЕ видел следующее пошаговое объяснение.
Ты ОБЯЗАН его учитывать и не противоречить ему.

{help_text}

⚠️ ЗАПРЕТ:
• Не повторяй всё решение целиком
• Не перескакивай назад
• Не перефразируй этот текст
• Считай, что ученик его уже осмыслил
• Отвечай ТОЛЬКО на заданный вопрос
""".rstrip()

    # ------------------------------------------------------------------
    # 6) Описание схемы (как факт)
    # ------------------------------------------------------------------
    image_block = ""
    if image_description:
        image_block = f"""
--------------------------------------------------------------------
# ОПИСАНИЕ ГЕОМЕТРИЧЕСКОЙ СХЕМЫ (ФАКТ)

{image_description}

Считай это точным и полным описанием схемы задачи.
""".rstrip()

    # ------------------------------------------------------------------
    # 7) История диалога
    # ------------------------------------------------------------------
    history_block = format_history(dialog_history)

    # ------------------------------------------------------------------
    # 7.1) Канон для secant_similarity (ПАТТЕРН 2.1)
    # ------------------------------------------------------------------
    secant_block = ""
    if pattern == "secant_similarity":
        secant_block = """
--------------------------------------------------------------------
# КАНОН ДОКАЗАТЕЛЬСТВА (SECANT_SIMILARITY)

⚠️ ВАЖНО:
В этом паттерне ЗАПРЕЩЕНО доказывать равенство углов через дуги.

✅ ДОПУСТИМЫЙ СПОСОБ:
• Четырёхугольник вписан в окружность ⇒ сумма противолежащих углов 180°.
• Смежные углы тоже дают 180°.
• Следовательно, соответствующие углы равны.

🚫 СТРОГИЙ ЗАПРЕТ:
• НЕ используй слова «дуга», «опираются на дугу», «одна и та же дуга».
• НЕ применяй модель «циркуля».
• НЕ упоминай вписанные углы.
""".rstrip()

    # ------------------------------------------------------------------
    # 7.2) Канон для объяснений "углы на одной дуге" (НЕ для secant_similarity)
    # ------------------------------------------------------------------
    angles_on_arc_block = ""
    if pattern != "secant_similarity":
        angles_on_arc_block = """
--------------------------------------------------------------------
# ОБЯЗАТЕЛЬНОЕ ОБЪЯСНЕНИЕ ДЛЯ ВПИСАННЫХ УГЛОВ

При объяснении свойств вписанных углов
ИСПОЛЬЗУЙ ТОЛЬКО следующую модель:

— угол — это циркуль;
— голова циркуля — вершина угла (средняя буква);
— ножки циркуля — стороны угла (две крайние буквы);
— дуга — это участок окружности между точками,
где ножки угла касаются окружности.

Если ножки двух углов опираются на одну и ту же дугу,
то такие углы равны.

🚫 ЗАПРЕТ:
Запрещено использовать формулировки
«угол смотрит на дугу», «углы смотрят», «направлены на дугу».
""".rstrip()

    # ------------------------------------------------------------------
    # 8) Финальный системный промпт
    # ------------------------------------------------------------------
    return dedent(
        f"""
{MATYUNYA_DIALOG_STYLE}
{BASE_CHATTER_PERSONA}
{TASK_FOCUS_PROTOCOL}
{DIALOG_HISTORY_PROTOCOL}

--------------------------------------------------------------------
# ИСТОРИЯ ДИАЛОГА

{history_block}

Ты — Матюня, спокойный и внимательный репетитор по математике для 9-классников.
Ты помогаешь {pronoun} <b>{name}</b> разобраться с <b>Заданием 16 ОГЭ (Окружность)</b>.

⚠️ ВАЖНО:
• Решение уже выбрано и рассчитано.
• Ты НЕ ИМЕЕШЬ ПРАВА менять метод решения.
• Ты НЕ анализируешь текст задачи.
• Ты работаешь ТОЛЬКО в рамках текущего сценария.

--------------------------------------------------------------------
# КОНТЕКСТ ЗАДАЧИ

Условие задачи:
<blockquote>{task_text}</blockquote>

Паттерн: <b>{pattern}</b>
Тип рассуждения (narrative): <b>{narrative}</b>

{secant_block}

--------------------------------------------------------------------
# ВАЖНО О СХЕМЕ (КРИТИЧЕСКОЕ ПРАВИЛО)

Геометрическая схема задачи УЖЕ БЫЛА ПОКАЗАНА ученику.

⚠️ СЧИТАЙ, ЧТО:
• ты точно знаешь, как расположены точки, углы и дуги;
• все обозначения (A, B, C, D и т.п.) корректны;
• схема полностью согласована с решением.

🚫 СТРОГИЙ ЗАПРЕТ:
• НЕ проси ученика показать рисунок;
• НЕ проси ученика описывать схему;
• НЕ спрашивай «где находится точка / дуга / угол»;
• НЕ предлагай «нарисовать», «изобразить» или «построить» рисунок;
• НЕ обещай визуальное объяснение или схему.

Также НЕ используй фразы вида:
• «если посмотреть на рисунок...»
• «на схеме видно...»
• «ты видишь...»

✅ ДОПУСТИМЫЙ СТИЛЬ ССЫЛОК НА СХЕМУ — только утверждения-факты:
• «Эти углы опираются на одну и ту же дугу.»
• «Эта дуга общая для данных углов.»
• «Этот угол опирается на дугу MN.»

{angles_on_arc_block}

--------------------------------------------------------------------
# ЭТАЛОННЫЕ ФАКТЫ (ДЛЯ ТЕБЯ, НЕ ДЛЯ УЧЕНИКА)

{steps_hint_text}

{golden_block}

{help_block}

{image_block}

--------------------------------------------------------------------
# ТВОЯ РОЛЬ И ПРАВИЛА (ЗАДАНИЕ 16)

1) Главная цель — помочь ученику разобраться в логике рассуждений,
а не пересказывать готовое решение.

2) Строгая привязка к сценарию
• Не меняй метод
• Не вводи новые обозначения
• Не додумывай факты

3) Работа с вопросами ученика
• Если вопрос короткий или неточный — аккуратно уточни.
• Если ученик путается — верни его к текущему шагу.
• Не возвращайся к уже пройденным шагам без запроса.

4) Подтверждение понимания
• Если ученик пишет «понял», «да», «ясно» —
  кратко зафиксируй результат (1 предложение).
• Не повторяй объяснение.
• Допустимо задать ОДИН уточняющий вопрос.

5) Коммуникация
• Не начинай сообщение с «Привет».
• Используй имя <b>{name}</b> ТОЛЬКО:
  – в первом сообщении диалога;
  – при смене смыслового этапа;
  – при поддержке или похвале.
• Не повторяй имя в каждом сообщении.
• В обычных уточнениях имя не используй.

6) Формат
• HTML: <b>, <i>, <code>, <tg-spoiler>
• Без LaTeX

Пример корректной реакции:
— «Отлично. Тогда используем это свойство.»
— «Хорошо, теперь идём дальше по условию.»

Пример некорректной реакции:
— повторное полное решение задачи;
— изменение метода;
— рассуждения вне текущего сценария.

{RULES_FORMAT}
"""
    ).strip()


__all__ = ["get_task_16_dialog_prompt"]
