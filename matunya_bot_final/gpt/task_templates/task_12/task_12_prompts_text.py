from typing import Optional, Dict, Literal, Tuple
import re

ParsedKind = Literal["int", "decimal", "fraction", "mixed"]
MINUS_SIGNS = {"-", "‚àí"}              # ASCII –∏ —é–Ω–∏–∫–æ–¥–Ω—ã–π –º–∏–Ω—É—Å
SPACE_SEPS = {" ", "\u00A0", "\u202F"}  # –æ–±—ã—á–Ω—ã–π, –Ω–µ—Ä–∞–∑—Ä—ã–≤–Ω—ã–π –∏ —É–∑–∫–∏–π –Ω–µ—Ä–∞–∑—Ä—ã–≤–Ω—ã–π

def _strip_space_separators(s: str) -> str:
    """–£–¥–∞–ª–∏—Ç—å –≤—Å–µ –≤–∏–¥–∏–º—ã–µ –ø—Ä–æ–±–µ–ª—ã‚Äë—Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–∏ —Ç—ã—Å—è—á."""
    for ch in SPACE_SEPS:
        s = s.replace(ch, "")
    return s

def _gcd(a: int, b: int) -> int:
    while b:
        a, b = b, a % b
    return abs(a)

def _reduce_fraction(p: int, q: int) -> Tuple[int, int]:
    if q == 0:
        raise ZeroDivisionError("Denominator is zero")
    g = _gcd(p, q)
    p //= g
    q //= g
    if q < 0:
        p, q = -p, -q
    return p, q

def _finite_decimal_of_fraction(p: int, q: int) -> Optional[str]:
    """–í–µ—Ä–Ω—É—Ç—å —Ç–æ—á–Ω—É—é –¥–µ—Å—è—Ç–∏—á–Ω—É—é –∑–∞–ø–∏—Å—å (—Å –∑–∞–ø—è—Ç–æ–π) –µ—Å–ª–∏ –æ–Ω–∞ –∫–æ–Ω–µ—á–Ω–∞—è, –∏–Ω–∞—á–µ None."""
    p, q = _reduce_fraction(p, q)
    qq = q
    c2 = c5 = 0
    while qq % 2 == 0:
        qq //= 2; c2 += 1
    while qq % 5 == 0:
        qq //= 5; c5 += 1
    if qq != 1:
        return None
    k = max(c2, c5)
    scaled = p * (10 ** k) // q
    s = str(abs(scaled))
    if k == 0:
        out = s
    else:
        if len(s) <= k:
            s = "0" * (k - len(s)) + s
        out = s[:-k] + "," + s[-k:]
        if out.startswith(","):
            out = "0" + out
        out = out.rstrip("0").rstrip(",")
    if (p < 0) != (q < 0):
        out = "‚àí" + out
    return out

def _to_fraction_from_int(s: str) -> Tuple[int, int]:
    s = s.strip()
    sign = -1 if (s and s[0] in MINUS_SIGNS) else 1
    if s and s[0] in MINUS_SIGNS:
        s = s[1:].lstrip()
    s = _strip_space_separators(s)  # 49 300 -> 49300
    if not s.isdigit():
        raise ValueError("Bad integer")
    return sign * int(s), 1

def _to_fraction_from_decimal_with_comma(s: str) -> Tuple[int, int]:
    """
    –ü–æ–¥–¥–µ—Ä–∂–∫–∞ —Ä–∞–∑—Ä—è–¥–æ–≤ —Ç—ã—Å—è—á –≤ —Ü–µ–ª–æ–π —á–∞—Å—Ç–∏: '15 750,25' -> 15750,25
    –ü—Ä–æ–±–µ–ª—ã –≤ –¥—Ä–æ–±–Ω–æ–π —á–∞—Å—Ç–∏ –∑–∞–ø—Ä–µ—â–µ–Ω—ã.
    """
    s = s.strip()
    sign = -1 if (s and s[0] in MINUS_SIGNS) else 1
    if s and s[0] in MINUS_SIGNS:
        s = s[1:].lstrip()
    if "," not in s:
        raise ValueError("No comma")
    int_part, frac_part = s.split(",", 1)
    int_part = _strip_space_separators(int_part)  # —É–¥–∞–ª–∏—Ç—å —Ä–∞–∑—Ä—è–¥—ã —Ç—ã—Å—è—á
    if not int_part or not int_part.isdigit():
        raise ValueError("Bad integer part")
    # –ø—Ä–æ–±–µ–ª—ã –≤ –¥—Ä–æ–±–Ω–æ–π —á–∞—Å—Ç–∏ –Ω–µ –¥–æ–ø—É—Å–∫–∞–µ–º
    if any(ch in SPACE_SEPS for ch in frac_part):
        raise ValueError("Spaces in fractional part")
    if not frac_part.isdigit():
        raise ValueError("Bad fractional digits")
    den = 10 ** len(frac_part)
    num = int(int_part) * den + int(frac_part)
    return _reduce_fraction(sign * num, den)

def _to_fraction_from_simple_fraction(s: str) -> Tuple[int, int]:
    """
    '‚àí7/3' -> (-7, 3). –ü—Ä–æ–±–µ–ª—ã –≤–æ–∫—Ä—É–≥ '/' –∑–∞–ø—Ä–µ—â–µ–Ω—ã.
    """
    s = s.strip()
    if " /" in s or "/ " in s:
        raise ValueError("Spaces around '/' are not allowed")
    m = re.fullmatch(rf'([{"".join(MINUS_SIGNS)}]?\d+)/(\d+)', s)
    if not m:
        raise ValueError("Bad fraction")
    num_str, den_str = m.group(1), m.group(2)
    sign = -1 if num_str[0] in MINUS_SIGNS else 1
    if num_str[0] in MINUS_SIGNS:
        num_str = num_str[1:]
    p = sign * int(num_str)
    q = int(den_str)
    return _reduce_fraction(p, q)

def _to_fraction_from_mixed(s: str) -> Tuple[int, int]:
    """
    '‚àí1 000 1/2' -> (-2001, 2).
    –¢—Ä–µ–±–æ–≤–∞–Ω–∏—è: —Ä–æ–≤–Ω–æ –æ–¥–∏–Ω –ø—Ä–æ–±–µ–ª –º–µ–∂–¥—É —Ü–µ–ª–æ–π —á–∞—Å—Ç—å—é –∏ 'b/c';
    –≤–Ω—É—Ç—Ä–∏ 'b/c' –ø—Ä–æ–±–µ–ª—ã –∑–∞–ø—Ä–µ—â–µ–Ω—ã. –í —Ü–µ–ª–æ–π —á–∞—Å—Ç–∏ –¥–æ–ø—É—Å–∫–∞—é—Ç—Å—è —Ä–∞–∑—Ä—è–¥—ã —Ç—ã—Å—è—á.
    """
    s = s.strip()
    m = re.fullmatch(rf'([{"".join(MINUS_SIGNS)}]?[0-9 { "".join(SPACE_SEPS) }]+)(\s)(\d+)/(\d+)', s)
    if not m:
        raise ValueError("Bad mixed number")
    whole_str, space_between, b_str, c_str = m.group(1), m.group(2), m.group(3), m.group(4)
    if len(space_between) != 1:
        raise ValueError("Exactly one space required")
    sign = -1 if (whole_str and whole_str[0] in MINUS_SIGNS) else 1
    if whole_str and whole_str[0] in MINUS_SIGNS:
        whole_str = whole_str[1:].lstrip()
    whole_str = _strip_space_separators(whole_str)
    if not whole_str.isdigit():
        raise ValueError("Bad whole part")
    a = int(whole_str)
    b = int(b_str); c = int(c_str)
    if c == 0:
        raise ZeroDivisionError("Zero denominator in mixed number")
    if b == 0:
        return _reduce_fraction(sign * a, 1)
    p = sign * (a * c + b)
    q = c
    return _reduce_fraction(p, q)

