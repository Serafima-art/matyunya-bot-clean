def get_random_feedback(
    name: Optional[str] = None,
    gender: Optional[str] = None,
    help_opened: bool = False,
) -> str:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—â—É—é —Ñ—Ä–∞–∑—É –ø–æ—Å–ª–µ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞.
    """

    # 1Ô∏è‚É£ –ë–∞–∑–æ–≤—ã–π –ø—É–ª —Ñ—Ä–∞–∑
    if help_opened:
        all_phrases = PHRASES_WITH_CLOSE_HELP.copy()
    else:
        all_phrases = PHRASES_NO_HELP.copy()

    # 2Ô∏è‚É£ –î–æ–±–∞–≤–ª—è–µ–º –∏–º–µ–Ω–Ω—ã–µ / –≥–µ–Ω–¥–µ—Ä–Ω—ã–µ
    if name:
        if gender == "–¥–µ–≤–æ—á–∫–∞":
            all_phrases.extend(p.format(name=name) for p in FEMALE_PHRASES)
        elif gender == "–º–∞–ª—å—á–∏–∫":
            all_phrases.extend(p.format(name=name) for p in MALE_PHRASES)
        else:
            all_phrases.extend([
                f"{name}, –æ—Ç–ª–∏—á–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç!",
                f"–¢–∞–∫ –¥–µ—Ä–∂–∞—Ç—å, {name}!",
            ])

    phrase = random.choice(all_phrases)

    # 3Ô∏è‚É£ –ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è "–ì–æ—Ç–æ–≤(-–∞)"
    if "–ì–æ—Ç–æ–≤(-–∞)" in phrase:
        if gender == "–¥–µ–≤–æ—á–∫–∞":
            phrase = phrase.replace("–ì–æ—Ç–æ–≤(-–∞)", "–ì–æ—Ç–æ–≤–∞")
        else:
            phrase = phrase.replace("–ì–æ—Ç–æ–≤(-–∞)", "–ì–æ—Ç–æ–≤")

    return phrase
