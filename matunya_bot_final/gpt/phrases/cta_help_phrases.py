def choose_cta_done(name: Optional[str] = None, gender: Optional[str] = None, rnd: Optional[random.Random] = None) -> str:
    rng = rnd or random
    tmpl = rng.choice(CTA_PHRASES_DONE)
    return _fmt(tmpl, name, gender)


def choose_cta_more(name: Optional[str] = None, gender: Optional[str] = None, rnd: Optional[random.Random] = None) -> str:
    rng = rnd or random
    tmpl = rng.choice(CTA_PHRASES_MORE)
    return _fmt(tmpl, name, gender)


def cta_done_from_fsm(state: Mapping[str, object], rnd: Optional[random.Random] = None) -> str:
    name = (state.get("student_name") if state else None) or None
    gender = (state.get("gender") if state else None) or None
    return choose_cta_done(name=name if isinstance(name, str) else None,
                           gender=gender if isinstance(gender, str) else None,
                           rnd=rnd)


